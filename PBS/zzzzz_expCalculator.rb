def calcExp(defeatedBattler, pkmn, expvar, partyLength)
  return 0 if pkmn[:level] - defeatedBattler[:level] >= 3
  a = defeatedBattler[:level] * defeatedBattler[:pokemon][:base_exp]
  exp = (a / partyLength).floor * partyLength # change is minor but does exist due to .floor
  exp = (exp * (100 + expvar * (defeatedBattler[:level] - pkmn[:level])) / 100).floor
  exp = (exp / 2).floor if pkmn[:level] > 40
  return exp
end

def simBattle(pkmn, trainers, expvar, partyLength, difficulty_mode, pokemon_data)
  cap = {0 => 100, 1 => 200, 2 => 300} # quite possibly one of the most sloppy ways to fix this, but i dont care
  trainers.each do |trainer|
    diff = (100 * difficulty_mode)
    trainerID = trainer[:id] + diff
    next if trainerID >= cap[difficulty_mode]
    selectedTrainer = trainers.find { |t| t[:name] == trainer[:name] && t[:id] == trainerID }
    if selectedTrainer.nil?
      puts "    ERROR: Trainer #{trainer[:name]} with ID #{trainerID} not found."
      next
    end
    selectedTrainer[:pokemon].each do |defeatedBattler|
      if pokemon_data.key?(defeatedBattler[:name])
        base_exp = pokemon_data[defeatedBattler[:name]][:base_exp]
        defeatedBattler_data = {
          level: defeatedBattler[:level],
          pokemon: { base_exp: base_exp }
        }
        expGained = calcExp(defeatedBattler_data, pkmn, expvar, partyLength)
        pkmn[:current_exp] += expGained
        pkmn[:total_exp_earned] += expGained
        while pkmn[:current_exp] >= simLevelUp(pkmn)
          pkmn[:level] += 1
          pkmn[:current_exp] -= simLevelUp(pkmn)
        end
        pkmn[:current_exp] = [pkmn[:current_exp], 0].max
      else
        puts "    ERROR: #{defeatedBattler[:name]} not found in the database."
      end
    end
  end
end

def simRoute(pokemon_team, location, obligatoryonly, expvar, difficulty_mode, trainers_data, pokemon_data)
  trainers = trainers_data[location]
  unless obligatoryonly
    trainers = trainers.select { |trainer| trainer[:obligatory] }
  end
  partyLength = pokemon_team.length
  pokemon_team.each do |pkmn|
    simBattle(pkmn, trainers, expvar, partyLength, difficulty_mode, pokemon_data)
  end

  puts "    Simulation Results:"
  msg = "      "
  pokemon_team.each do |pkmn|
    puts "      #{pkmn[:name]} - Level: #{pkmn[:level]}, Current EXP: #{pkmn[:current_exp]}, Total EXP Earned: #{pkmn[:total_exp_earned]}"
    msg += "#{pkmn[:name]} #{pkmn[:level]} #{pkmn[:current_exp]},"
  end
  puts msg
end

def simLevelUp(pkmn)
  n = pkmn[:level]
  case pkmn[:growth_rate]
  when "Medium"
    exp = (n**3).floor
  when "Erratic"
    if n <= 50
      exp = ((n**3 * (100 - n)) / 50).floor
    elsif n <= 68
      exp = ((n**3 * (150 - n)) / 100).floor
    elsif n <= 98
      p = [0.000, 0.008, 0.014]
      exp = (n**3 * 1.274 - (n / 150.0) - p[n % 3]).floor
    else
      exp = ((n**3 * (160 - n)) / 100).floor
    end
  when "Fluctuating"
    if n <= 15
      exp = ((n**3 * ((24 + (n + 1) / 3)) / 50)).floor
    elsif n <= 36
      exp = ((n**3 * (14 + n) / 50)).floor
    else
      exp = ((n**3 * ((32 + n / 2) / 50))).floor
    end
  when "Parabolic"
    exp = ((6 * n**3) / 5 - 15 * n**2 + 100 * n - 140).floor
  when "Fast"
    exp = ((4 * n**3) / 5).floor
  when "Slow"
    exp = ((5 * n**3) / 4).floor
  else
    print "    ERROR: why are you stupid?"
    exp = 99 ** 99
  end
  return exp
end

def input_pkmndata(pokemon_data)
  pokemon_team = []
  puts "    Enter the names and levels in the format 'NAME LEVEL START_EXP' (e.g., 'CHIXULOB 50 1745, TITANOTROP 55 0')."
  puts "    PS: pkmn names are case unsensitve. If no level/start exp is given, they will default to level 1/start exp 0"
  input = gets.chomp
  team_size = input.split(',').first(6)
  team_size.each do |poke|
    name, level, explefties = poke.strip.split
    name = name.upcase
    level = level.to_i
    level = [[level, 1].max, 100].min
    explefties = explefties.to_i
    explefties = [explefties, 0].max
    if pokemon_data.key?(name)
      pokemon_team << { name: name, level: level, 
                        current_exp: explefties, total_exp_earned: 0, 
                        base_exp: pokemon_data[name][:base_exp], 
                        growth_rate: pokemon_data[name][:growth_rate] }
    else
      puts "    ERROR: #{name} not found in the database. Please try again."
      return input_pkmndata(pokemon_data)
    end
  end
  return pokemon_team
end

def input_trainerdata(trainers_data)
  puts "    Enter the experience variable (33 or 50):"
  expvar = gets.chomp.to_i
  #----------------
  puts "    Enter the location (case sensitive):"
  location = gets.chomp
  unless trainers_data.key?(location)
    puts "    ERROR: Location not found in the database. Please try again."
    return input_trainerdata(trainers_data)
  end
  #----------------
  puts "    Do you want to fight all trainers? (y/n):"
  fight_all_trainers_input = gets.chomp.downcase
  obligatoryonly = fight_all_trainers_input == "y"
  #----------------
  puts "    What difficulty mode? (0 = casual, 1 = hard, 2 = chaos):"
  difficulty_mode = gets.chomp.to_i
  difficulty_mode = [[difficulty_mode, 0].max, 2].min
  return [location, obligatoryonly, difficulty_mode, expvar]
end

